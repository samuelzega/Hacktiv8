function graduates(students) {
  var siswaLulus = {}
  for (const i of students) {
    if (!siswaLulus[i.class]) {
      siswaLulus[i.class] = []
    }
    if (i.score > 75) {
      siswaLulus[i.class].push({
        name: i.name,
        score: i.score
      })
    }
  }
  

  return siswaLulus;
}

